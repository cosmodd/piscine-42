#include <unistd.h>

void	ft_print_comb(void)
{
	char	out[3];

	out[0] = '0';
	out[1] = '1';
	out[2] = '2';
	while (out[0] != '7')
	{
		if (++out[2] > '9')
		{
			out[2] = '0';
			out[1]++;
		}
		if (out[1] > '9')
		{
			out[1] = '0';
			out[0]++;
		}
		if (out[0] < out[1] && out[1] < out[2])
		{
			write(1, &out, 3);
			write(1, ", ", 2);
		}
	}
	write(1, "789", 3);
}
