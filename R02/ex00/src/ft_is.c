int	is_numeric(char	c)
{
	return (c >= '0' && c <= '9');
}

int	is_space(char c)
{
	return ((c >= 9 && c <= 13) || c == ' ');
}

int	ft_str_is_printable(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
		if (str[i] < ' ' || str[i] > '~')
			return (0);
	return (1);
}
