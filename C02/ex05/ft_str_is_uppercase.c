int	ft_str_is_uppercase(char *str)
{
	char	c;
	int		i;

	i = -1;
	while (str[++i] != '\0')
	{
		c = str[i];
		if (c < 'A' || c > 'Z')
			return (0);
	}
	return (1);
}
